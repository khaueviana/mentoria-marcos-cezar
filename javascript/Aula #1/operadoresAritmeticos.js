//Operadores aritméticos básicos
var soma = 1 + 2;
console.log(soma);
var subtração = 10 - 3;
console.log(subtração);
var multiplicacao = 3 * 3;
console.log(multiplicacao);
var divisao = 10 / 2;
console.log(divisao);

//Operadores aritméticos abreviados
var somaUm = 10 + 10;
somaUm++; // igual a soma = soma +1
console.log(somaUm);
++somaUm;
console.log(somaUm);

var subtraiUm = 22;
subtraiUm--; //igual a sub = sub - 1
console.log(subtraiUm);
--subtraiUm;
console.log(subtraiUm);

var somaAbreviada = 20;
somaAbreviada += 20; //igual a soma = soma + 20
console.log(somaAbreviada);

var subAbreviada = 40;
subAbreviada -= 20; //mesmo que sub = sub - 20;
console.log(subAbreviada);

var multiAbreviada = 10;
multiAbreviada *= 10; //O mesmo que multi = multi * 10
console.log(multiAbreviada);

var divisaoAbreviada = 20;
divisaoAbreviada /= 4; //O mesmo que div = div / 4
console.log(divisaoAbreviada);





